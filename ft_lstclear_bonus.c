#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list *l;

	while (*lst)
	{
		del((*lst)->content);
		l = (*lst)->next;
		free(*lst);
		*lst = l;
	}
}
